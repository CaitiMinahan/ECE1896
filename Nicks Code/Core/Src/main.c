void WakeBQ79616(void)
{
	PIN_STMTX_Location = 10;
	GPIOC->MODER &= ~(0b11 << (PIN_STMTX_Location*2)); // clear the GPIO MODE to 00
	GPIOC->MODER |= (0b10 << (PIN_STMTX_Location*2)); // Sets the mode of PC10 to alternate function
	GPIOC->AFR[2] &= ~(0b1111<<(12)); //sets Alternate function for PC10 to AF8
	GPIOC->ODR &= ~(0b1 <<(PIN_STMTX_Location)); // output 0 when in gpio mode
	GPIOC->MODER ^= (0b11 << (PIN_STMTX_Location*2)); // GPIO MODE
	//HAL_Delay(2); // Use Veronica's Delay Function
	DELAY_US(2500);
	DELAY_US(2500);
	GPIOC->MODER ^= (0b11 << (PIN_STMTX_Location*2)); // AF

}


